void 
VIC::setColor(int nr, uint8_t red, uint8_t green, uint8_t blue, uint8_t alpha)
{
	uint8_t *color = (uint8_t *)(&colors[nr]);

	assert(nr >= 0 && nr < 16);
	
	color[0] = red;
	color[1] = green;
	color[2] = blue;
	color[3] = alpha;
}

void 
VIC::setColorScheme(ColorScheme scheme)
{
	uint8_t i, r, g, b;
	
	if (scheme == CUSTOM_PALETTE)
		return;

	for (i = 0; i < 16; i++) {
		getColor(scheme, i, &r, &g, &b);
		setColor(i, r, g, b);
	}

}			